class Namess
{
	public static void main(String[] args)
	{
		char name[][]={{'j','a','n','a','n'},{'d','h','a','r','s'},{'s','a','h','n','a'},{'k','a','r','t','h'}};
		for(int i=0;i<name.length;i++)
		{
			for(int j=0;j<name[i].length;j++)
			{
				System.out.print(name[i][j]+ " ");
			}
			System.out.println("");
			
		}
	}
}