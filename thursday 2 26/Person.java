class person
{
	private  String name;
	private int age;
	
	public person()
	{
		name="geeks";
		age=10;
	}
	
	public String getname()
	{
		return name;
	}
	
	public void setname(String name)
	{
		this.name=name;
	}
	
	public int getage()
	{
		return age;
	}
	
	public void setage(int age)
	{
		this.age=age;
	}
}